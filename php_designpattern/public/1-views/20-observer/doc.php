    <h4>Định nghĩa</h4>
    <p>Observer Pattern là một trong những Pattern thuộc nhóm hành vi (Behavior Pattern).
        Nó định nghĩa mối phụ thuộc một – nhiều giữa các đối tượng để khi mà một đối tượng có sự thay đổi trạng thái,
        tất các thành phần phụ thuộc của nó sẽ được thông báo và cập nhật một cách tự động.
    </p>
    <hr>
    <h4>Sử dụng khi nào ?</h4>
    <p>
        Khi thay đổi một đối tượng, yêu cầu thay đổi đối tượng khác và chúng ta không biết có bao nhiêu đối tượng cần thay đổi, những đối tượng này là ai.
        Sử dụng để quản lý sự kiện (Event management).
    </p>
</div>