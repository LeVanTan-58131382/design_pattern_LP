    <h4>Định nghĩa</h4>
    <p>Mediator Pattern là một trong những Pattern thuộc nhóm hành vi (Behavior Pattern).
        Mediator có nghĩa là người trung gian. Pattern này nói rằng “Định nghĩa một đối tượng
        gói gọn cách một tập hợp các đối tượng tương tác.
        Mediator thúc đẩy sự khớp nối lỏng lẻo (loose coupling) bằng cách ngăn không cho các đối tượng
        đề cập đến nhau một cách rõ ràng và nó cho phép bạn thay đổi sự tương tác của họ một cách độc lập”</p>
    <hr>
    <h4>Sử dụng khi nào ?</h4>
    <p>
        Điều chỉnh hành vi giữa các lớp một cách dễ dàng, không cần chỉnh sửa ở nhiều lớp.
        Khi cần tái sử dụng một đối tượng nhưng rất khó khăn vì nó tham chiếu và giao tiếp với nhiều đối tượng khác.
        Khi giao tiếp giữa các object trong hệ thống quá phức tạp, có quá nhiều quan hệ giữa các object trong hệ thống.
        Một điểm chung để kiểm soát hoặc giao tiếp là cần thiết.
    </p>
