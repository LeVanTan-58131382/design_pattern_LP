    <h4>Định nghĩa</h4>
    <p>Null Object Pattern là một trong những Pattern thuộc nhóm hành vi (Behavior Pattern).
        Null Object pattern không phải là một Gang of Four Design Pattern
        Tư tưởng của Null Object là sử dụng một đối tượng Null đặc biệt để gói gọn sự vắng mặt của một thể hiện
        bằng cách cung cấp một sự thay thế hành xử theo cách thụ động phù hợp
    </p>
    <hr>
    <h4>Sử dụng khi nào ?</h4>
    <p>
        Đối phó với các đối tượng null.
        Thay vì kiểm tra đối tượng null, chúng ta xác định hành vi null hoặc hành vi được gọi nhưng không làm gì.
        Cung cấp hành vi, giá trị mặc định trong trường hợp dữ liệu không có sẵn.
        Tạo các đối tượng để thử nghiệm, trong trường hợp tài nguyên thật không có sẵn.
    </p>
