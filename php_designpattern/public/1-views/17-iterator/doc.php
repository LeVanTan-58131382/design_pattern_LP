        <h4>Định nghĩa</h4>
        <p>Iterator Pattern là một trong những Pattern thuộc nhóm hành vi (Behavior Pattern). Nó được sử dụng để “Cung cấp một cách thức truy cập tuần tự tới các phần tử của một đối tượng tổng hợp, mà không cần phải tạo dựng riêng các phương pháp truy cập cho đối tượng tổng hợp này”. </p>
        <hr>
        <h4>Sử dụng khi nào ?</h4>
        <p>1. Cần truy cập nội dung của đối tượng trong tập hợp mà không cần biết nội dung cài đặt bên trong nó.</p>
        <p>2. Hỗ trợ truy xuất nhiều loại tập hợp khác nhau.</p>
        <p>3. Cung cấp một interface duy nhất để duyệt qua các phần tử của một tập hợp.</p>
